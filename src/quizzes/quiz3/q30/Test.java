package quizzes.quiz3.q30;

public class Test {
    public static void main(String[] args) {
        // ASCII code of 'a' is 97
        char c1 = 'a'; 
        // Range of char data type is from 0 to 65535 and hence it can be easily assigned to int type.            
        int i1 = c1;
        // println() method is overloaded to accept char type and int type both
        System.out.println(i1); // Print 97
    }
    
}
